clc;
clear;
close all;

% ------------------------------------------------
% Sampling Parameters
% ------------------------------------------------
fs = 1000;
t = 0:1/fs:2-1/fs;

% ------------------------------------------------
% Create Exact Time-Varying Signal
% ------------------------------------------------
x = zeros(size(t));

% -----------------------------------------------
% First Half (0–1 sec)
% 50 Hz + 150 Hz
% -----------------------------------------------
idx1 = t < 1;

x(idx1) = sin(2*pi*50*t(idx1)) + ...
          sin(2*pi*150*t(idx1));

% -----------------------------------------------
% Second Half (1–2 sec)
% 150 Hz + 200 Hz
% -----------------------------------------------
idx2 = t >= 1;

x(idx2) = sin(2*pi*150*t(idx2)) + ...
          sin(2*pi*200*t(idx2));

% ------------------------------------------------
% FFT
% ------------------------------------------------
N = length(x);

X = fft(x);

f = (0:N-1)*(fs/N);

mag = abs(X)/N;

% ------------------------------------------------
% Plot Signal
% ------------------------------------------------
figure;

subplot(2,1,1)

plot(t,x)

xlabel('Time (s)')
ylabel('Amplitude')

title('Time-Varying Signal')

grid on

% ------------------------------------------------
% FFT Plot
% ------------------------------------------------
subplot(2,1,2)

plot(f(1:N/2),mag(1:N/2),...
    'LineWidth',1.5)

xlabel('Frequency (Hz)')
ylabel('Magnitude')

title('FFT Spectrum')

xlim([0 300])

grid on